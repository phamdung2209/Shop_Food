<h2 class="sapo">Ngày 14/6/2015 vừa qua Công ty cổ phần trực tuyến Đăng Quang vinh dự được trao chứng nhận “ Thương hiệu, sản phẩm và dịch vụ xuất sắc năm 2014 – 2015” </h2>

<div class="noidung">
    <p>
        <a href="https://dantri.com.vn/thi-truong/dang-quang-watch-duoc-vinh-danh-trong-gala-bao-chi-voi-doanh-nhan-doanh-nghiep-2015080707545808.htm" target="_blank">&gt;&gt; Link chi tiết xem tại Báo Dân trí</a>
    </p>

    <p>Sáng ngày 14/6/2015, Lễ Vinh Danh “Thương hiệu – Sản Phẩm – Dịch Vụ xuất sắc năm 2014 -2015” đã long trọng diễn ra tại Nhà Hát Lớn, Hà Nội.&nbsp;Chương trình diễn ra nhằm vinh danh những doanh nhân, doanh nghiệp tiêu biểu năm 2014 - 2015. Đồng thời trao bảng vàng lưu danh cho "Doanh nhân, doanh nghiệp, Nhà quản lý và Nhà hoạt động xã hội tiêu biểu thời hội nhập 2014 - 2015. Gala cũng dành nguồn quỹ trao học bổng cho sinh viên nghèo vượt khó, học giỏi.</p>

    <p style="text-align:center">
        <a href="https://www.dangquangwatch.vn/lib/ckfinder/images/IMG_3639(1).jpg" class="example-image-link countimg0" data-lightbox="example-1"><img alt="" src="https://www.dangquangwatch.vn/lib/ckfinder/images/IMG_3639(1).jpg" style="height:333px; width:500px" data-lightbox="roadtrip"></a>
    </p>

    <p>Chương trình Gala “ Báo chí với doanh nhân, doanh nghiệp” do tạp chí Doanh nghiệp và Hội nhập, Hiệp hội doanh nghiệp Việt Nam…phối hợp cùng tổ chức. Chương trình có sự tham dự của đại diện lãnh đạo Đảng và nhà nước, lãnh đạo các Bộ, Ban, Ngành TW, các tổ chức phi chính phủ cùng Doanh nhân, Doanh nghiệp trong và ngoài nước.</p>

    <p style="text-align:center">
        <a href="https://www.dangquangwatch.vn/lib/ckfinder/images/IMG_9655s(1).jpg" class="example-image-link countimg1" data-lightbox="example-1"><img alt="" src="https://www.dangquangwatch.vn/lib/ckfinder/images/IMG_9655s(1).jpg" style="height:533px; width:500px" data-lightbox="roadtrip"></a>
    </p>

    <p>Chương trình nhằm tôn vinh và khen thưởng những Doanh nghiệp tiêu biểu, sản xuất hiệu quả và bền vững. Các thương hiệu và sản phẩm đáp ứng các tiêu chí cơ bản như: là sản phẩm có uy tín trên thị trường và được tin dùng, chấp hành tốt chính sách và pháp luật, tích cực tham gia các hoạt động vì sự phát triển cộng đồng…</p>

    <p style="text-align:center">
        <a class="example-image-link countimg2" href="https://dangquangwatch.vn/lib/ckfinder/images/IMG_9678s.jpg" data-lightbox="example-1"><img alt="" src="https://dangquangwatch.vn/lib/ckfinder/images/IMG_9678s.jpg" style="height:308px; line-height:20.7999992370605px; text-align:center; width:500px" data-lightbox="roadtrip"></a>
    </p>

    <p>Đây là chương trình đặc sắc, hoành tráng và trang trọng, có sức lan tỏa lớn, giúp Doanh nghiệp khẳng định thương hiệu của mình. Là một Doanh nghiệp uy tín cung cấp sản phẩm Đồng hồ đeo tay chính hãng trên thị trường Việt Nam với phương châm “đáp ứng mọi yêu cầu dù khắt khe nhất của khách hàng”. Công ty luôn duy trì được tốc độ tăng trưởng cao, toàn diện về mọi mặt một cách bền vững so với các công ty kinh doanh cùng lĩnh vực . Đăng Quang Watch vinh dự nhận được giải thưởng “ thương hiệu, sản phẩm và dịch vụ xuất sắc năm 2014 – 2015 “ đó là nhờ sự tin tưởng và ủng hộ từ phía khách hàng.</p>

    <p style="text-align:center">
        <a href="https://www.dangquangwatch.vn/lib/ckfinder/images/DSC_1342(1).JPG" class="example-image-link countimg3" data-lightbox="example-1"><img alt="Đăng Quang Watch được vinh danh trong Gala “Báo chí với doanh nhân, doanh nghiệp”" src="https://www.dangquangwatch.vn/lib/ckfinder/images/DSC_1342(1).JPG" style="height:355px; width:500px" data-lightbox="roadtrip"></a>
